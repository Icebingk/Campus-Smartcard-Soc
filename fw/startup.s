# ============================================================================
# 启动汇编 — PicoRV32 RV32EC
# ============================================================================
# 功能: 设置栈指针, 清零 .bss, 跳转到 main()
# ============================================================================

.section .text.start, "ax", @progbits
.globl _start
.type _start, @function

_start:
    # 设置栈指针 (由链接脚本定义)
    la sp, _estack

    # 清零 .bss 段
    la t0, _sbss
    la t1, _ebss
1:
    bgeu t0, t1, 2f
    sw zero, 0(t0)
    addi t0, t0, 4
    j 1b
2:

    # 跳转到 main()
    call main

    # main 返回后死循环
3:
    wfi
    j 3b
