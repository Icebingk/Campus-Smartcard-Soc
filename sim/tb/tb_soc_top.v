// ============================================================================
// Testbench: 全芯片顶层仿真 (SoC Top)
// ============================================================================
// 测试场景:
//   1. 上电复位 & 时钟启动
//   2. CPU 行为模型自动执行自检序列 (ROM/SRAM/APB外设/越界)
//   3. 中断验证 (基带/AES/EEPROM)
//   4. 射频接口回环测试
//
// 版本: v1.0
// 负责人: 阿呆不呆
// ============================================================================

`timescale 1ns / 1ps

module tb_soc_top ();

    // ================================================================
    // 时钟与复位
    // ================================================================
    reg clk_sys;
    reg rst_n;

    // ─── 时钟生成 (13.56 MHz, T ≈ 73.87 ns) ───
    initial begin
        clk_sys = 1'b0;
        forever #36.935 clk_sys = ~clk_sys;
    end

    // ─── 复位 ───
    initial begin
        rst_n = 1'b0;
        #200 rst_n = 1'b1;   // 200ns 后释放复位
    end

    // ================================================================
    // 射频接口激励
    // ================================================================
    reg  rf_rx;
    wire rf_tx;
    reg  rf_clk;
    reg  sleep;

    // ─── 射频时钟 (13.56 MHz，与系统同频) ───
    initial begin
        rf_clk = 1'b0;
        forever #36.935 rf_clk = ~rf_clk;
    end

    // ─── 射频接收数据 (模拟读卡器发送的指令) ───
    initial begin
        rf_rx = 1'b0;
        sleep = 1'b0;
        #5000;  // 等待 CPU 自检完成
        // 模拟一段曼彻斯特编码数据 (简化)
        rf_rx = 1'b1;  #73.87;
        rf_rx = 1'b0;  #73.87;
        rf_rx = 1'b1;  #73.87;
        rf_rx = 1'b0;  #73.87;
        rf_rx = 1'b0;  #73.87;
        rf_rx = 1'b1;  #73.87;
        rf_rx = 1'b0;  #73.87;
        rf_rx = 1'b1;  #73.87;
    end

    // ================================================================
    // 测试接口
    // ================================================================
    reg  test_mode;
    reg  scan_clk;
    reg  scan_in;
    wire scan_out;

    initial begin
        test_mode = 1'b0;
        scan_clk  = 1'b0;
        scan_in   = 1'b0;
    end

    // ================================================================
    // 例化 SoC 顶层
    // ================================================================
    soc_top uut (
        .clk_sys   (clk_sys),
        .rst_n     (rst_n),
        .rf_rx     (rf_rx),
        .rf_tx     (rf_tx),
        .rf_clk    (rf_clk),
        .sleep     (sleep),
        .test_mode (test_mode),
        .scan_clk  (scan_clk),
        .scan_in   (scan_in),
        .scan_out  (scan_out)
    );

    // ================================================================
    // 仿真控制
    // ================================================================
    initial begin
        $display("[TB_TOP] ========================================");
        $display("[TB_TOP]  校园智能卡 SoC 全芯片仿真");
        $display("[TB_TOP]  时间: %0t", $time);
        $display("[TB_TOP] ========================================");

        // 等待 CPU 完成自检并挂起 (约 300 个时钟周期)
        #30000;

        $display("\n[TB_TOP] ===== 仿真完成 =====");
        $display("[TB_TOP] 请查看波形确认各模块行为");
        $finish;
    end

    // ================================================================
    // 波形 dump
    // ================================================================
    initial begin
        $dumpfile("tb_soc_top.vcd");
        $dumpvars(0, tb_soc_top);
    end

endmodule
